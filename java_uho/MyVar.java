public class MyVar
{
	public static void main(String [] j)
	{
		int uang;
		boolean benar=true;
		char huruf='f';
		double pecahan=3.14;
		
		uang=1000;
		float satuSetengah=1.5f; //huruf 'f' melambangkan penggunaan bit sebanyak 32 bit
		
		System.out.println("Uang 	: "+uang);
		System.out.println("Huruf 	: "+huruf);
		System.out.println("Pecahan : "+pecahan);
		System.out.println("Benar 	: "+benar);
		System.out.println(satuSetengah);
	}
}